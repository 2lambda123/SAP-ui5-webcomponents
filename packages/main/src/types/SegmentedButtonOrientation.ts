/**
 * Different SegmentedButton orientations.
 *
 * @readonly
 * @enum {string}
 * @public
 * @author SAP SE
 * @alias sap.ui.webc.main.types.SegmentedButtonOrientation
 */
enum SegmentedButtonOrientation {
	/**
	 * Horizontal orientation. Segmented Button items are placed in horizontal direction (one after another)
	 * @public
	 * @type {Horizontal}
	 */
	Horizontal = "Horizontal",

	/**
	 * Vertical orientation. Segmented Button items are placed in vertical direction (one above another)
	 * @public
	 * @type {Vertical}
	 */
	Vertical = "Vertical",
}

export default SegmentedButtonOrientation;
